[1, 2] + [3, 4]                       # конкатенация
[7, 8] * 3                            # повторение
a += b                                # расширить список a
'----------------------'
# добавление элементов
numbers.append(21)                    # добавить в конец
numbers.extend([34, 55])              # расширить списком
numbers.extend('abc')                 # расширить строкой → ['a','b','c']
numbers.insert(0, 'first')            # вставить по индексу
'----------------------'
# удаление элементов
del numbers[5]                        # удалить по индексу
del numbers[2:7]                      # удалить срез
numbers.remove('first')               # удалить первый по значению
item = numbers.pop()                  # удалить и вернуть последний
item = numbers.pop(1)                 # удалить и вернуть по индексу
numbers.clear()                       # очистить весь список
'----------------------'
# поиск и подсчёт
pos = numbers.index('value')          # индекс первого вхождения
cnt = numbers.count('value')          # количество вхождений

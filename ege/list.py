numbers[0], numbers[-1]               # первый, последний элемент
numbers[1:3]                          # срез [1, 2]
numbers[1] = 101                      # изменение (списки изменяемы!)
numbers[2:5] = ['a', 'b']             # изменение среза
'----------------------'

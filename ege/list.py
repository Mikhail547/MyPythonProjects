# изменение порядка
numbers.reverse()                     # обратить на месте
numbers.sort()                        # сортировать по возрастанию
numbers.sort(reverse=True)            # по убыванию
sorted_copy = sorted(numbers)         # новый отсортированный список
'----------------------'
# копирование
copy = numbers.copy()                 # поверхностная копия
copy = numbers[:]                     # то же через срез
copy = list(numbers)                  # то же через list()
'----------------------'
# перебор элементов
for item in numbers: ...              # по элементам
for i in range(len(numbers)): ...     # по индексам
for i, item in enumerate(numbers): ...# индекс + элемент
'----------------------'
# вывод
print(*numbers)                       # 1 2 3 через пробел
print(*numbers, sep='\n')             # каждый на новой строке

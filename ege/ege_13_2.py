# Данные из условия задачи
ip = [191, 89, 109, 206]
mask = [255, 255, 224, 0]

# 1. Находим адрес сети с помощью побитового И (&)
net = [ip[i] & mask[i] for i in range(4)]

# 2. Находим широковещательный адрес с помощью побитового ИЛИ (|)
# Конструкция (255 - mask[i]) инвертирует маску (превращает 0 в 1)
broadcast = [net[i] | (255 - mask[i]) for i in range(4)]

# 3. Наибольший адрес компьютера — это широковещательный адрес минус 1
max_computer_ip = broadcast.copy()
max_computer_ip[3] -= 1

# Вывод результатов
print("Адрес сети:", net)                  # [191, 89, 96, 0]
print("Широковещательный:", broadcast)    # [191, 89, 127, 255]
print("Макс. узел:", max_computer_ip)       # [191, 89, 127, 254]
print("Сумма октетов:", sum(max_computer_ip)) # 661

def R(a, b):
    if a == b:
        return 1
    if a > b:
        return 0

    # Команда А: Прибавь 1 (выполняется всегда)
    routes = R(a + 1, b)

    # Выделяем цифры десятков и единиц
    s = str(a)
    des = s[-2]  # десяткам соответствует предпоследний символ
    ed = s[-1]  # единицам соответствует последний символ

    # Команда B: Поменяй местами (только если цифра десятков меньше цифры единиц)
    if des < ed:
        # Собираем новое число: всё до десятков + единица + десятка
        new_a = int(s[:-2] + ed + des)
        routes += R(new_a, b)

    return routes


print(R(100, 150))



# Отрезок B от 70 до 90 включительно
B = set(range(70, 91))


# Функция для ДЕЛ(n, m)
def DEL(n, m):
    return n % m == 0


# Перебираем варианты для А (ищем наибольшее, поэтому можно взять с запасом)
for A in range(1, 200):
    all_x_valid = True

    # Проверяем выражение для большого диапазона x (например, от 1 до 1000)
    for x in range(1, 1000):
        # Исходное выражение: DEL(x, A) or ((x in B) -> (not DEL(x, 16)))
        # В Python импликация (X -> Y) записывается как (not X or Y)
        f = DEL(x, A) or (not (x in B) or not DEL(x, 16))

        if not f:
            all_x_valid = False
            break  # Если хотя бы для одного x ложно, этот А не подходит

    # Если выражение истинно для ВСЕХ x, выводим А
    if all_x_valid:
        print(f"Подходящее А: {A}")
